export function tFeed(msg) {
  if (lang === 'ko' || !msg) return msg
  let out = msg
  for (const [ko, en] of FEED_PARTS) out = out.split(ko).join(en)
  return out
}

export function t(str) {
  if (lang === 'ko' || str == null) return str
  return DICT[str] ?? str
}

